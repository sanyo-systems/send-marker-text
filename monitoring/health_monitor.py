import time
import logging

# ==========================================================
# システム生存確認ログ (Heartbeat)
#
# プログラムが正常稼働していることをログに定期記録する。
# 現場運用ではこのログを確認することで
# 「プログラムが停止していないか」を判断できる。
#
# 30分ごとに SYSTEM_HEARTBEAT を出力する。
#
# 用途
# ・長時間稼働監視
# ・ログ確認による動作確認
# ・システム停止の早期発見
# ==========================================================
def heartbeat_loop():

    while True:
        # プログラム停止検知用ログ
        logging.info("SYSTEM_HEARTBEAT")
        # 30分間隔で行う
        time.sleep(1800)